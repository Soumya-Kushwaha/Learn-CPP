#include <iostream>

int main(){
    std::cout<<"The size of char datatype is: "<<sizeof(char)<<" byte."<<std::endl;
    std::cout<<"The size of int datatype is: "<<sizeof(int)<<" bytes."<<std::endl;
    std::cout<<"The size of float datatype is: "<<sizeof(float)<<" bytes."<<std::endl;
    std::cout<<"The size of double datatype is: "<<sizeof(double)<<" bytes."<<std::endl;
    return 0;
}